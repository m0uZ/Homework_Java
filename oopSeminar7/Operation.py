def div_operation(a, b):
    return "{:1.1f} / {:1.1f} = {:1.2f}".format(a, b, a / b)

def mult_operation(a, b):
    return "{:1.1f} * {:1.1f} = {:1.2f}".format(a, b, a * b)

def sub_operation(a, b):
    return "{:1.1f} - {:1.1f} = {:1.2f}".format(a, b, a - b)

def sum_operation(a, b):
    return "{:1.1f} + {:1.1f} = {:1.2f}".format(a, b, a + b)


